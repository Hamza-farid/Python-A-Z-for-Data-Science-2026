x = 'hamza farid'



print(x.upper())  # converts to uppercase
print(x.lower())  # converts to lowercase

print(x.title())  # converts to title case
print(x.capitalize())  # converts to capitalized case

print(x.replace('a', 'u'))  # replaces 'a' with 'o'

print(x.replace('a', 'u', 1))  # replaces first occurrence of 'a' with 'o'

print(x.replace('a', 'u', 2))  # replaces first two occurrences of 'a' with 'o'

# string into array
# here space is a delimeter

y = x.split(" ") 
print(y)


